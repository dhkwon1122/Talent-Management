with st.expander("📐 점수 계산 방식 상세 보기"):
    st.markdown(
        """
        ### 공통 파이프라인

        ```
        Raw CSV → 피처 집계 → 차원별 가중합(원점수) → Min-Max 정규화 + √변환 → 0~100점
        ```

        모든 차원은 동일한 정규화 공식을 적용합니다.

        $$\\text{정규화 점수} = \\sqrt{\\frac{x - x_{min}}{x_{max} - x_{min}}} \\times 100$$

        > 루트 변환을 통해 하위 점수대의 가독성을 높이고, 최상위 집중 현상을 완화합니다.

        ---

        ### 업무성과 — 평가등급 점수화 로직

        연도별 인사평가 등급을 수치로 환산한 뒤, **최근 연도에 높은 가중치**를 부여해 합산합니다.

        | 등급 | 환산 점수 |
        |------|-----------|
        | 가   | 100점 |
        | 나   | 80점  |
        | 다   | 60점  |
        | 라   | 40점  |
        | 마   | 20점  |

        **가중 평균 공식** (n년치 데이터, 오래된 순 1·2·…·n 선형 가중치)

        $$\\text{grade\\_score} = \\frac{\\sum_{i=1}^{n} i \\times \\text{score}_i}{\\sum_{i=1}^{n} i}$$

        예) 3년 데이터 [다, 나, 가] → (1×60 + 2×80 + 3×100) / 6 = **86.7점**

        최종 업무성과 원점수 = `grade_score × 0.50 + KPI × 0.25 + 주관과제 × 0.15 + 예산 × 0.10`

        ---

        ### 리더십 · 협업네트워크 — LLM 텍스트 분석

        동료/리더십 코멘트를 **Google Gemini API**로 분석해 0~10점 산출.
        API 키 미설정 시 키워드 기반 로컬 점수로 자동 대체되며,
        결과는 `data/processed/text_scores.json`에 캐싱됩니다.

        ---

        ### 성장잠재력 — KPI 성장률

        최근 3년 평균 KPI vs. 이전 기간 평균 KPI의 **증감률(%)**을 사용합니다.

        $$\\text{growth\\_rate} = \\frac{\\text{KPI}_{\\text{최근 3년}} - \\text{KPI}_{\\text{이전}}}{\\text{KPI}_{\\text{이전}}}$$
        """
    )

